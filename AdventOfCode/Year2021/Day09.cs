        internal class Solution1 : IAdventDaySolution
        {
            private readonly int[,] _heightMap;
            private readonly (int Length, int Width) _dimensions;

            public Solution1(int[,] heightMap)
            {
                _heightMap = heightMap;
                _dimensions = (Length: _heightMap.GetLength(0), Width: _heightMap.GetLength(1));
            }

            public string Description => @"Find all of the low points on your heightmap. What is the sum of the risk levels of all low points on your heightmap?";
            public int Part => 1;
            public object Solve()
            {
                return _heightMap
                    .Cast<int>()
                    .Select((v, i) => (Value: v, Row: i / _dimensions.Width, Column: i % _dimensions.Width))
                    .Where(i => (i.Row <= 0 || _heightMap[i.Row - 1, i.Column] > i.Value)
                                && (i.Row >= _dimensions.Length - 1 || _heightMap[i.Row + 1, i.Column] > i.Value)
                                && (i.Column <= 0 || _heightMap[i.Row, i.Column - 1] > i.Value)
                                && (i.Column >= _dimensions.Width - 1 || _heightMap[i.Row, i.Column + 1] > i.Value))
                    .Sum(v => v.Value + 1);
            }
        }

        internal class Solution2 : IAdventDaySolution
        {
            private readonly int[,] _heightMap;
            private readonly (int Length, int Width) _dimensions;

            public Solution2(int[,] heightMap)
            {
                _heightMap = heightMap;
                _dimensions = (Length: _heightMap.GetLength(0), Width: _heightMap.GetLength(1));
            }

            public string Description => @"What do you get if you multiply together the sizes of the three largest basins?";
            public int Part => 2;
            public object Solve()
            {
                return _heightMap
                    .Cast<int>()
                    .Select((v, i) => (Value: v, Row: i / _dimensions.Width, Column: i % _dimensions.Width))
                    .Where(i => (i.Row <= 0 || _heightMap[i.Row - 1, i.Column] > i.Value)
                                && (i.Row >= _dimensions.Length - 1 || _heightMap[i.Row + 1, i.Column] > i.Value)
                                && (i.Column <= 0 || _heightMap[i.Row, i.Column - 1] > i.Value)
                                && (i.Column >= _dimensions.Width - 1 || _heightMap[i.Row, i.Column + 1] > i.Value))
                    .Select(GetBasinSize)
                    .OrderByDescending(b => b)
                    .Take(3)
                    .Aggregate((a,b) => a*b);
            }

            private int GetBasinSize((int Value, int Row, int Column) lowPoint)
            {
                var locationsChecked = new HashSet<(int Row, int Column)>();
                return GetBasinSize(locationsChecked, (lowPoint.Row, lowPoint.Column));
            }

            private int GetBasinSize(ISet<(int Row, int Column)> locationsChecked, (int Row, int Column) location)
            {
                if (locationsChecked.Contains(location))
                {
                    return 0;
                }

                locationsChecked.Add(location);
                if (location.Row < 0 || location.Row > _dimensions.Length - 1 || location.Column < 0 || location.Column > _dimensions.Width - 1)
                {
                    return 0;
                }

                if (_heightMap[location.Row, location.Column] == 9)
                {
                    return 0;
                }

                return 1
                       + GetBasinSize(locationsChecked, (location.Row - 1, location.Column))
                       + GetBasinSize(locationsChecked, (location.Row + 1, location.Column))
                       + GetBasinSize(locationsChecked, (location.Row, location.Column - 1))
                       + GetBasinSize(locationsChecked, (location.Row, location.Column + 1));
            }
        }

