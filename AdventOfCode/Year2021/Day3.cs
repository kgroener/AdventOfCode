    class Day3 : IAdventDayPuzzle
    {
        public string Description => @"The submarine has been making some odd creaking noises, so you ask it to produce a diagnostic report just in case.

The diagnostic report (your puzzle input) consists of a list of binary numbers which, when decoded properly, can tell you many useful things about the conditions of the submarine. The first parameter to check is the power consumption.

You need to use the binary numbers in the diagnostic report to generate two new binary numbers (called the gamma rate and the epsilon rate). The power consumption can then be found by multiplying the gamma rate by the epsilon rate.

Each bit in the gamma rate can be determined by finding the most common bit in the corresponding position of all numbers in the diagnostic report.";

        public DateTime Date => new DateTime(2021, 12, 3);
        public IEnumerable<IAdventDaySolution> GetSolutions()
        {
            return new IAdventDaySolution[] { new Solution1(DiagnosticData, 12), new Solution2(DiagnosticData, 12) };
        }

        internal class Solution1 : IAdventDaySolution
        {
            private readonly ushort[] _data;
            private readonly byte _diagnosticSize;

            public Solution1(ushort[] data, byte diagnosticSize)
            {
                _data = data;
                _diagnosticSize = diagnosticSize;
            }

            public string Description => @"Use the binary numbers in your diagnostic report to calculate the gamma rate and epsilon rate, then multiply them together. What is the power consumption of the submarine?";
            public int Part => 1;
            public object Solve()
            {
                var gammaRate = _data
                    .Select(d => Enumerable.Range(0, _diagnosticSize).Select(i => (d >> i) & 1).ToArray())
                    .Aggregate((a, b) => a.Zip(b, (x, y) => x + y).ToArray())
                    .Select((d, i) => (d > _data.Length / 2 ? 1 : 0) << i)
                    .Sum();
                var epsilonRate = ~gammaRate & ((int)Math.Pow(2, _diagnosticSize)-1);

                return gammaRate * epsilonRate;
            }
        }

        internal class Solution2 : IAdventDaySolution
        {
            private readonly ushort[] _data;
            private readonly byte _diagnosticSize;

            public Solution2(ushort[] data, byte diagnosticSize)
            {
                _data = data;
                _diagnosticSize = diagnosticSize;
            }

            public string Description =>
                @"Use the binary numbers in your diagnostic report to calculate the oxygen generator rating and CO2 scrubber rating, then multiply them together. What is the life support rating of the submarine?";
            public int Part => 2;
            public object Solve()
            {
                int oxygen = GetLifeSupportData(true);
                int carbondioxide = GetLifeSupportData(false);

                return oxygen * carbondioxide;
            }

            private int GetLifeSupportData(bool removeLeastCommon)
            {
                var data = new List<ushort>(_data);
                int index = _diagnosticSize - 1;
                while (data.Count != 1)
                {
                    int mostCommon = data.Count(d => ((d >> index) & 1) == 1) >= data.Count / 2.0 ? 1 : 0;
                    data.RemoveAll(d => (((d >> index) & 1) == mostCommon) ^ removeLeastCommon);
                    index--;
                }

                return data[0];
            }
        }

